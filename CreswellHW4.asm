# Takes the story of Tarzan (the first 5000 characters) and outputs 
# the five most used letters and the number of times they are used.
# MIPS Assembly Code

.text
main:
	la $s2, array1	#address of array1
	la $s0, arrayCounters  # address of arrayCounters
	addi $t0, $zero, 26 # loop counter -- number of elements in arrayCounters
	add $t6, $zero, $zero  # Initialize maxCount to 0
	addi $s5, $zero, 5000 # set loop counter to 5000
	
top:	
	addi $s5, $s5, -1 # decrement counter by 1
	
	lw $t1, 0 ($s2) # read one ASCII character from data
	andi $t1, $t1, 0xffffffdf # change lowercase to uppercase
	subi $t1, $t1, 65 # 0x41 is the offset from 'A' = 0x41
	sll $t1, $t1, 2 # multiply index by 4 to get the byte address
	add $t2, $s0, $t1 # now t2 contains the byte address of the character
	lw $t3, 0($t2) # obtain the current count of the character
	addi $t3, $t3, 1 # increment character count 
	sw $t3, 0($t2) # store new count in memory
	addi $s2, $s2, 4
	
	bne $s5, $zero, top #jump to top if the counter is not 0

	addi $s6, $zero, 5 #counter for first loop
	L1:	
		addi $t0, $t0, -1  # decrement loop counter
		
		beqz $s6, end # go to end when counter is 0
		
		lw $t5, 0($s0)  # load data from array
	
		blt $t5, $t6, after  # branch if I do NOT have a new max
		add $t6, $t5, $zero   # here set the new max by copying
		add $t7, $s0, $zero   # address of max value found is in $t7

	after:  	
		addi $s0, $s0, 4  # increment by 1 word = 4 bytes
		bne $t0, $zero, L1

		# Print "Letter " 
		la $a0, text1  # address of the constant, defined string to be printed
		addi $v0, $zero, 4  # set up syscall for print a string
		syscall
	
		# Print the letter
		la $s0, arrayCounters  # address of arrayCounters
		sub $t4, $t7, $s0 # distance from top of array to element with most freq. occurrence
		la $t3, alphabet  # address of "alphabet" array -- that is, addr. of "alphabet[0]" element
		add $a0, $t4, $t3  # "address arithmetic" to find the address of "alphabet[i]" 
		addi $v0, $zero, 4 # set up syscall for print a string
		syscall
		
		# Print " has " 
		la $a0, text2  # address of the constant, defined string to be printed
		addi $v0, $zero, 4  # set up syscall for print a string
		syscall
		
		# Print number of occurencess
		li $v0, 1
		add $a0, $t6, $s1
		syscall 

		# Print " occurrences" 
		la $a0, text3  
		addi $v0, $zero, 4  
		syscall
		
		add $t6, $t5, $zero   # reset the maxCount
		sw $zero, 0($t7) # reset the number at the address of maxCount
	
		addi $t0, $zero, 26 # reset loop counter
		add $s6, $s6, -1 # decrement outer loop counter
		j L1 #jump back to the top to continue looping
	
end:
.data

